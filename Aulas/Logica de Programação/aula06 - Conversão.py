# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo para outro tipos imutáveis e primitivos:
# str, int, float, bool

print(int('1'), type(int('1'))) # 1 <class 'int'>
print(int('1') + 1) # 2
print(bool('')) # FALSE
print(bool('x')) # TRUE
print(str(11) + 'b') # 11b