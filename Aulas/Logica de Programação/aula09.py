# OPERAÇÕES ARITMÉTICOS

adicao = 10 + 10 
print('Adição:', adicao) # Adição: 20 | Operador -> +

subtracao= 15 - 5 
print('Subtração:', subtracao) # Subtração: 10 | Operador -> -

multiplicacao = 10 * 10
print('Multiplicação:', multiplicacao) # Multiplicação: 100 | Operador -> *

divisao = 10 / 2.2
print('Divisão:', divisao) # Divisão: 4.545454545454545 | Operador -> /

divisao_inteira = 10 // 2.2
print('Divisão inteira:',  divisao_inteira) # Divisão inteira: 4.0 | Operador -> //

modulo = 55 % 2 # Resto da divisão
print('Módulo:', modulo) # Módulo: 1 | Operador -> %

exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao) # Exponenciação: 1024 | Operador -> **