# Python = Linguagem de Programação
# Tipo de tipagem = Dinâmica / Forte
# str -> string -> texto
# Strings são textos que estão dentro de aspas
print(1234)

# Forma Mais utilizada
print('"String dentro das aspas duplas"') # "String dentro das aspas duplas" 
print("'String dentro das aspas simples'") # 'String dentro das aspas simples'

# Aspas Simples
print('String em aspas simples') # String em aspas simples

# Aspas Duplas
print("String em aspas duplas") # String em aspas duplas

# Escape
print("String com \" Escape \" ") # String com " Escape " 

# r
print(r"Luiz \"Otávio\"") # Luiz \"Otávio\"