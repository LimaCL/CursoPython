# Operadores de comparação (relacionais)
# OP      Significado         Exemplo (True)
# >       maior               2 > 1
# >=      maior ou igual      2 >= 2
# <       menor               1 < 2
# <=      menor ou igual      2 <= 2
# ==      igual               'a' == 'a'
# !=      diferente           'a' != 'b'

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print('Maior:', maior)                      # Maior: True
print('Maior ou Igual:', maior_ou_igual)    # Maior ou Igual: True
print('Menor:', menor)                      # Menor: True
print('Menor ou Igual:', menor_ou_igual)    # Menor ou Igual: True
print('Igual:', igual)                      # Igual: True
print('Diferente: ', diferente)             # Diferente:  True 